import java.util.*;
class EvenOdd
{
	public static void main(String args[])
	{
		Scanner s1=new Scanner(System.in);
		System.out.print("Enter the any numbers");
		int num=s1.nextInt();
		if(num%2==0)
		{
			System.out.print("it is even number");
		}
		else
		{
			System.out.print("it is odd numbers");
		}
	}
}
			